    def _construct_prompt(self, current_best_code: str, history: List[Dict[str, Any]], telemetry: Dict[str, Any]) -> str:
        """
        Constructs the massive state vector ($s_t$) prompt for the LLM.
        """
        prompt = f"### SYSTEM CONSTRAINTS\n{self.system_prompt}\n\n"
        prompt += f"### CURRENT BEST CODE (SOTA)\n```python\n{current_best_code}\n```\n\n"

        prompt += "### RECENT EXPERIMENTS HISTORY (Last K=32)\n"
        for exp in history:
            prompt += f"- Patch: {exp.get('patch', 'N/A')}\n"
            prompt += f"  Status: {exp.get('status', 'UNKNOWN')} | BPB: {exp.get('final_bpb', 'N/A')} | Reward: {exp.get('reward', 'N/A')}\n"

        prompt += "\n### TELEMETRY & DIAGNOSTICS\n"
        prompt += json.dumps(telemetry, indent=2)

        prompt += "\n\n### YOUR TASK\nGenerate a JSON array of search/replace patches to mutate the current best code to improve BPB."
        return prompt

    def generate_action(self, current_best_code: str, history: List[Dict[str, Any]], telemetry: Dict[str, Any]) -> str:
        """
        Takes the current state, queries the LLM, applies the diff, and returns the new candidate source code.
        """
        prompt = self._construct_prompt(current_best_code, history, telemetry)

        # MOCK LLM CALL
        logger.info("Querying Meta-Learner LLM for next action...")
        # Simulate LLM generating a patch to change MLP expansion from 3x to 4x
        mock_llm_response = '''
        ```json
        [
            {
                "search": "hidden_dim = 3 * config.n_embd",
                "replace": "hidden_dim = 4 * config.n_embd"
            }
        ]
        ```
        '''

        try:
            patches = DiffParser.parse_llm_json(mock_llm_response)
            new_code = current_best_code
            for patch in patches:
                new_code = DiffParser.apply_patch(new_code, patch["search"], patch["replace"])

            logger.info("Successfully applied generated patch.")
            return new_code

        except ValueError as e:
            logger.error(f"Action generation failed during patching: {e}")
            return current_best_code # Fallback to SOTA


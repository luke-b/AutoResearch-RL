class SemanticValidator(ast.NodeVisitor):
    def __init__(self):
        self.is_valid = True
        self.error = None

    def visit_Import(self, node):
        for name in node.names:
            if name.name in ['os', 'sys', 'subprocess']:
                self.is_valid = False
                self.error = f"Banned import detected: {name.name}"
        self.generic_visit(node)

    def visit_ImportFrom(self, node):
        if node.module in ['os', 'sys', 'subprocess']:
            self.is_valid = False
            self.error = f"Banned import detected: {node.module}"
        self.generic_visit(node)

    def visit_Call(self, node):
        if isinstance(node.func, ast.Name) and node.func.id in ['eval', 'exec', '__import__', 'open']:
            self.is_valid = False
            self.error = f"Banned built-in function call detected: {node.func.id}"
        self.generic_visit(node)

class ASTDiffParser:
    """
    Handles parsing and applying diff patches generated by the LLM using AST and structural node location.
    Provides safety against minor formatting changes and explicitly handles assignments or class modifications.
    """
    @staticmethod
    def apply_patch(original_code: str, search_block: str, replace_block: str) -> str:
        # Try AST Match first for simple assignments (e.g. Hyperparameter changes)
        try:
            search_tree = ast.parse(search_block)
            if len(search_tree.body) == 1 and isinstance(search_tree.body[0], ast.Assign):
                target_var = search_tree.body[0].targets[0].id

                # Parse full original code to find the exact line number of this variable assignment
                orig_tree = ast.parse(original_code)
                for node in ast.walk(orig_tree):
                    if isinstance(node, ast.Assign) and len(node.targets) == 1 and hasattr(node.targets[0], 'id'):
                        if node.targets[0].id == target_var:
                            # Found the line! Replace it precisely
                            lines = original_code.splitlines()
                            lines[node.lineno - 1] = replace_block.strip()
                            return "\n".join(lines)
        except Exception as e:
            logger.debug(f"AST-based assignment diff failed or wasn't applicable, trying structure matching: {e}")
            pass

        # Use AST based generic patch application
        try:
            # We attempt to find matching nodes in the original AST that structurally match the search block
            search_ast = ast.parse(search_block)
            original_ast = ast.parse(original_code)

            # Simple heuristic: if the search block is a single function/class def, try to find it by name
            if len(search_ast.body) == 1 and isinstance(search_ast.body[0], (ast.FunctionDef, ast.ClassDef)):
                target_name = search_ast.body[0].name
                for node in original_ast.body:
                    if isinstance(node, (ast.FunctionDef, ast.ClassDef)) and node.name == target_name:
                        # Before replacing, verify that the found node actually matches the search block's content
                        start_line = node.lineno - 1
                        end_line = node.end_lineno
                        lines = original_code.splitlines()
                        original_node_text = "\n".join(lines[start_line:end_line])

                        # Verify using robust whitespace-insensitive match to ensure we are replacing the right code
                        search_lines_norm = [line.strip() for line in search_block.splitlines() if line.strip()]
                        orig_node_lines_norm = [line.strip() for line in original_node_text.splitlines() if line.strip()]

                        # Only proceed if the original node matches the search block structurally
                        # This avoids replacing a class when the LLM hallucinated its current state
                        if search_lines_norm == orig_node_lines_norm:
                            new_lines = lines[:start_line] + replace_block.splitlines() + lines[end_line:]
                            return "\n".join(new_lines)
                        else:
                            raise ValueError(f"AST structural match found node {target_name}, but its content does not match the search block.")

            # More general AST structure matching could be implemented here,
            # but standard AST modules don't provide easy arbitrary block replacement
            # while maintaining exact formatting for the rest of the file.
        except Exception as e:
            logger.debug(f"AST-based structural diff failed, falling back to text match: {e}")

        # Fallback: Robust Whitespace-Insensitive Matching
        return ASTDiffParser._apply_text_patch(original_code, search_block, replace_block)

    @staticmethod
    def _apply_text_patch(original_code: str, search_block: str, replace_block: str) -> str:
        # 1. Exact Match Fast Path
        if search_block in original_code:
            return original_code.replace(search_block, replace_block, 1)

        # 2. Robust Whitespace-Insensitive Matching
        search_lines_norm = [line.strip() for line in search_block.splitlines() if line.strip()]
        orig_lines = original_code.splitlines()

        if not search_lines_norm:
            raise ValueError("Empty search block provided.")

        match_start_idx = -1
        match_end_idx = -1

        for i in range(len(orig_lines)):
            search_idx = 0
            curr_idx = i

            while curr_idx < len(orig_lines) and search_idx < len(search_lines_norm):
                if orig_lines[curr_idx].strip() == "":
                    curr_idx += 1
                    continue
                if orig_lines[curr_idx].strip() == search_lines_norm[search_idx]:
                    search_idx += 1
                    curr_idx += 1
                else:
                    break

            if search_idx == len(search_lines_norm):
                match_start_idx = i
                match_end_idx = curr_idx
                break

        if match_start_idx == -1:
            logger.error("Diff Patch Failed: Search block not found in original code even with flexible matching.")
            raise ValueError("Search block not found")

        new_lines = orig_lines[:match_start_idx] + replace_block.splitlines() + orig_lines[match_end_idx:]
        return "\n".join(new_lines)

    @staticmethod
    def parse_llm_json(llm_response: str) -> List[Dict[str, str]]:
        try:
            json_str = llm_response
            match = re.search(r'```json\s*(.*?)\s*```', llm_response, re.DOTALL)
            if match:
                json_str = match.group(1)
            else:
                # Try to extract the first JSON-like object/array if the response contains extra text.
                start_positions = [pos for pos in (json_str.find('['), json_str.find('{')) if pos != -1]
                if start_positions:
                    json_str = json_str[min(start_positions):]

            # Try to clean up common JSON issues (trailing commas, unescaped quotes in strings)
            # Remove trailing commas before closing brackets
            json_str = re.sub(r',(\s*[}\]])', r'\1', json_str)

            patches = json.loads(json_str)
            if not isinstance(patches, list):
                patches = [patches]

            # Schema validation and normalization
            for patch in patches:
                if not isinstance(patch, dict):
                    raise ValueError("Patch must be a JSON object.")

                if 'search' in patch and 'replace' in patch:
                    if not isinstance(patch['search'], str) or not isinstance(patch['replace'], str):
                        raise ValueError("Patch 'search' and 'replace' values must be strings.")
                elif patch.get('op') in ('replace', 'add', 'remove') or patch.get('operation') in ('replace', 'add', 'remove'):
                    # Normalize 'operation' to 'op' for consistency
                    if 'operation' in patch and 'op' not in patch:
                        patch['op'] = patch['operation']
                    if 'path' not in patch:
                        raise ValueError("Patch missing required 'path' field.")
                    # 'value' is optional for remove operations
                    if patch.get('op') != 'remove' and 'value' not in patch:
                        raise ValueError("Patch missing required 'value' field.")
                else:
                    raise ValueError("Patch missing 'search'/'replace' or 'op'/'operation'/'path'/'value' keys.")

            return patches
        except json.JSONDecodeError as e:
            logger.error(f"Failed to parse LLM patch output: {e}")
            raise ValueError("Invalid JSON patch format")

    @staticmethod
    def _apply_json_patch(original_code: str, patch: Dict[str, Any]) -> str:
        if patch.get("op") != "replace":
            raise ValueError(f"Unsupported JSON patch op: {patch.get('op')}")

        path = patch.get("path")
        value = patch.get("value")
        if not isinstance(path, str):
            raise ValueError("Patch path must be a string.")

        # Extract the variable/attribute name from the path.
        # Paths can be like: "mlp_expansion", "GPTConfig().mlp_expansion", "src/GPTConfig().mlp_expansion"
        # We want the last identifier component
        import re as regex
        match = regex.search(r'(\w+)\s*$', path)
        if not match:
            raise ValueError(f"Could not extract identifier from path: {path}")
        target = match.group(1)

        # If value is scalar, replace assignment in the current module/class context
        if not isinstance(value, str):
            # Attempt to replace a simple assignment or constant value
            return ASTDiffParser._replace_assignment(original_code, target, value)

        # For string values, attempt to replace a function or body block.
        if target.isidentifier():
            # If path refers to a method or function, replace that body
            return ASTDiffParser._replace_function_or_assignment(original_code, target, value)

        raise ValueError("Unable to apply JSON patch to the provided path/value.")


    @staticmethod
    def _replace_assignment(original_code: str, target: str, new_value: Any) -> str:
        try:
            tree = ast.parse(original_code)
            lines = original_code.splitlines()
            for node in ast.walk(tree):
                if isinstance(node, ast.Assign) and len(node.targets) == 1 and hasattr(node.targets[0], "id"):
                    if node.targets[0].id == target:
                        # Preserve indentation from the original line
                        old_line = lines[node.lineno - 1]
                        leading_whitespace = len(old_line) - len(old_line.lstrip())
                        indent = old_line[:leading_whitespace]
                        value_text = repr(new_value) if not isinstance(new_value, str) else f'"{new_value}"'
                        lines[node.lineno - 1] = f"{indent}{target} = {value_text}"
                        return "\n".join(lines)
        except Exception:
            pass
        raise ValueError(f"Could not replace assignment for target '{target}'")

    @staticmethod
    def _replace_function_or_assignment(original_code: str, path: str, new_body: str) -> str:
        tree = ast.parse(original_code)
        lines = original_code.splitlines()
        path_parts = path.split(".")

        if len(path_parts) >= 2:
            # Class method replacement like src/ClassName.method
            class_name = path_parts[-2]
            func_name = path_parts[-1]
            for node in ast.walk(tree):
                if isinstance(node, ast.ClassDef) and node.name == class_name:
                    for child in node.body:
                        if isinstance(child, ast.FunctionDef) and child.name == func_name:
                            return ASTDiffParser._replace_function_body(lines, child, new_body)

        # Otherwise, try top-level function replacement
        func_name = path_parts[-1]
        for node in ast.walk(tree):
            if isinstance(node, ast.FunctionDef) and node.name == func_name:
                return ASTDiffParser._replace_function_body(lines, node, new_body)

        # If no function found, fallback to assignment replacement by target name
        return ASTDiffParser._replace_assignment(original_code, func_name, new_body)

    @staticmethod
    def _replace_function_body(lines: List[str], node: ast.FunctionDef, new_body: str) -> str:
        indent = " " * (node.col_offset + 4)
        body_lines = [line.rstrip() for line in new_body.splitlines()]
        # Dedent the incoming body if it contains leading spaces
        import textwrap
        body_lines = [indent + line for line in textwrap.dedent("\n".join(body_lines)).splitlines()]
        if not body_lines:
            body_lines = [indent + "pass"]

        start = node.lineno
        end = node.end_lineno
        new_function = [lines[node.lineno - 1]] + body_lines
        result = lines[: start] + new_function + lines[end:]
        return "\n".join(result)


class PolicyValueNetwork(nn.Module):
    """
    Basic PPO Actor-Critic Network handling an extended state vector.
    """
    def __init__(self, state_dim=13):
        super().__init__()
        self.actor = nn.Sequential(
            nn.Linear(state_dim, 64),
            nn.ReLU(),
            nn.Linear(64, 1),
            nn.Sigmoid()
        )
        self.critic = nn.Sequential(
            nn.Linear(state_dim, 64),
            nn.ReLU(),
            nn.Linear(64, 1)
        )

    def forward(self, state):
        action_mean = self.actor(state)
        value = self.critic(state)
        return action_mean, value

class PPOMetaAgent:
    def __init__(self, system_prompt: str = "Minimize BPB under 16MB. Modify architecture hyperparameters within GPTConfig like mlp_expansion, depth_loops, lora_rank, qk_gain_init, and muon_lr."):
        self.system_prompt = system_prompt
        self.hf_token = os.environ.get("HF_TOKEN")
        self.model_id = "Qwen/Qwen3-4B-Instruct-2507:nscale"  # Paid inference model

        if self.hf_token:
            logger.info("HF_TOKEN found. Hugging Face Router API activated.")
            from openai import OpenAI
            self.client = OpenAI(
                base_url="https://router.huggingface.co/v1",
                api_key=self.hf_token,
            )
        else:
            logger.info("No HF_TOKEN found. Operating in MOCK mode.")
            self.client = None

        self.state_dim = 13
        self.policy_net = PolicyValueNetwork(state_dim=self.state_dim)
        self.optimizer = optim.Adam(self.policy_net.parameters(), lr=1e-3)
        self.gamma = 0.99
        self.eps_clip = 0.2
        self.rollout_memory = []

    def _extract_vector_state(self, current_code: str, env_history: List[Dict[str, Any]], telemetry: Dict[str, Any]) -> torch.Tensor:
        """
        Builds a rich state vector.
        Features: SOTA BPB, Iteration, Memory Size, OOM Flag, Recent Delta Trend, SPRT Abort Freq, Extracted AST Params.
        """
        current_sota = 1.0
        memory_size = len(env_history)
        oom_flag = 1.0 if telemetry.get("recent_oom", False) else 0.0
        iteration = telemetry.get("iteration", 0) / 100.0

        # Parse current code for Hyperparams
        lora_rank_val = 0.0
        mlp_exp_val = 0.0
        depth_loops_val = 0.0
        qk_gain_init_val = 0.0
        muon_lr_val = 0.0
        try:
            tree = ast.parse(current_code)
            for node in ast.walk(tree):
                if isinstance(node, ast.Assign) and len(node.targets) == 1 and hasattr(node.targets[0], 'id'):
                    if node.targets[0].id == "lora_rank" and isinstance(node.value, ast.Constant):
                        lora_rank_val = float(node.value.value)
                    if node.targets[0].id == "mlp_expansion" and isinstance(node.value, ast.Constant):
                        mlp_exp_val = float(node.value.value)
                    if node.targets[0].id == "depth_loops" and isinstance(node.value, ast.Constant):
                        depth_loops_val = float(node.value.value)
                    if node.targets[0].id == "qk_gain_init" and isinstance(node.value, ast.Constant):
                        qk_gain_init_val = float(node.value.value)
                    if node.targets[0].id == "muon_lr" and isinstance(node.value, ast.Constant):
                        muon_lr_val = float(node.value.value)
        except: pass

        # Calculate Trends
        abort_freq = 0.0
        delta_trend = 0.0
        avg_abort_step = 0.0
        if env_history:
            valid_bpbs = [e.get("final_bpb") for e in env_history if e.get("final_bpb") is not None and not torch.isnan(torch.tensor(e.get("final_bpb")))]
            if valid_bpbs:
                current_sota = min(valid_bpbs)

            # Abort freq and avg abort step
            aborts = [e for e in env_history if e.get("status") == "ABORTED"]
            abort_freq = len(aborts) / float(max(1, len(env_history)))

            if aborts:
                avg_abort_step = sum([e.get("components", {}).get("sprt_abort_penalty", 0.0) for e in aborts]) / len(aborts)

            # Reward Trend
            recent_rewards = [e.get("reward", 0.0) for e in env_history[-3:]]
            if recent_rewards:
                delta_trend = sum(recent_rewards) / len(recent_rewards)

        state_vec = [
            current_sota, float(iteration), oom_flag, float(memory_size),
            abort_freq, delta_trend, lora_rank_val, mlp_exp_val,
            depth_loops_val, qk_gain_init_val, muon_lr_val, avg_abort_step,
            float(len(env_history) > 0 and any("causality" in e.get("components", {}) for e in env_history[-5:]))
        ]
        return torch.tensor(state_vec, dtype=torch.float32)

    def _construct_prompt(self, current_best_code: str, history: List[Dict[str, Any]], telemetry: Dict[str, Any]) -> str:
        prompt = f"### SYSTEM CONSTRAINTS\n{self.system_prompt}\n\n"
        prompt += f"### CURRENT BEST CODE (SOTA)\n```python\n{current_best_code}\n```\n\n"
        prompt += "### RECENT EXPERIMENTS HISTORY (Formalized Ontology)\n"

        # Formalized grouping of experiment history for better retrieval and pattern recognition by the LLM
        successful_experiments = [e for e in history if e.get("status") == "COMPLETED" and e.get("final_bpb") is not None]
        failed_experiments = [e for e in history if e.get("status") != "COMPLETED"]

        prompt += f"#### SUCCESSFUL SOTA/IMPROVEMENT ATTEMPTS ({len(successful_experiments)})\n"
        for exp in successful_experiments:
            prompt += f"- [Category: {exp.get('category', 'N/A')}] Patch: {exp.get('patch', 'N/A')}\n"
            prompt += f"  BPB: {exp.get('final_bpb', 'N/A')} | Reward: {exp.get('reward', 'N/A')}\n"

        prompt += f"\n#### FAILED/ABORTED EXPERIMENTS (Anti-Regression Memory - {len(failed_experiments)})\n"
        for exp in failed_experiments[-10:]: # Only show last 10 to save context
            prompt += f"- [Category: {exp.get('category', 'N/A')}] Patch: {exp.get('patch', 'N/A')}\n"
            prompt += f"  Status: {exp.get('status', 'UNKNOWN')}\n"
            if exp.get('remediation'):
                prompt += f"  Remediation: {exp.get('remediation')}\n"

        prompt += "\n### TELEMETRY & DIAGNOSTICS\n"
        prompt += json.dumps(telemetry, indent=2)
        prompt += "\n\n### YOUR TASK\nGenerate a JSON array of search/replace patches to mutate the current best code to improve BPB. Use the ontology of past successes and failures to inform your next structured proposal. Output the JSON in a code block like ```json\n[...]\n```"
        return prompt

    def generate_action(self, current_best_code: str, history: List[Dict[str, Any]], telemetry: Dict[str, Any]) -> str:
        prompt = self._construct_prompt(current_best_code, history, telemetry)
        state_tensor = self._extract_vector_state(current_best_code, history, telemetry)

        with torch.no_grad():
            action_mean, state_value = self.policy_net(state_tensor)
            dist = torch.distributions.Normal(action_mean, 0.1)
            action_temp = dist.sample()
            llm_temperature = torch.clamp(action_temp, 0.1, 1.5).item()
            log_prob = dist.log_prob(action_temp)

        self.rollout_memory.append({
            "state": state_tensor,
            "action": action_temp,
            "log_prob": log_prob,
            "value": state_value
        })

        logger.info(f"Querying Meta-Learner LLM (PPO Temp: {llm_temperature:.2f} | SPRT Freq: {state_tensor[4]:.2f})...")

        llm_response = ""
        if self.client:
            try:
                messages = [
                    {"role": "system", "content": "You are a code-mutating PPO agent optimized to output JSON diff patches."},
                    {"role": "user", "content": prompt}
                ]
                completion = self.client.chat.completions.create(
                    model=self.model_id,
                    messages=messages,
                    temperature=llm_temperature,
                    max_tokens=1000
                )
                llm_response = completion.choices[0].message.content
            except Exception as e:
                logger.error(f"Hugging Face Router API call failed: {e}")
                return current_best_code
        else:
            llm_response = '''
            ```json
            [
                {
                    "search": "    mlp_expansion = 3",
                    "replace": "    mlp_expansion = 4"
                }
            ]
            ```
            '''

        try:
            patches = ASTDiffParser.parse_llm_json(llm_response)
            new_code = current_best_code
            for patch in patches:
                if "search" in patch and "replace" in patch:
                    new_code = ASTDiffParser.apply_patch(new_code, patch["search"], patch["replace"])
                elif patch.get("op") == "replace" and "path" in patch and "value" in patch:
                    new_code = ASTDiffParser._apply_json_patch(new_code, patch)
                else:
                    raise ValueError("Patch missing required keys.")

            # AST-preserving transform validation
            tree = ast.parse(new_code)

            # Semantic Safety Validation
            validator = SemanticValidator()
            validator.visit(tree)
            if not validator.is_valid:
                raise ValueError(f"Semantic safety check failed: {validator.error}")

            logger.info("Successfully applied generated patch.")
            return new_code

        except (ValueError, SyntaxError) as e:
            logger.error(f"Action generation failed during patching or syntax validation: {e}")
            return current_best_code

    def update_policy(self, final_reward: float):
        # We append the received reward to the latest memory slot
        if self.rollout_memory:
            self.rollout_memory[-1]["reward"] = final_reward

        # Batch size for PPO updates
        BATCH_SIZE = 8
        if len(self.rollout_memory) < BATCH_SIZE:
            logger.info(f"Accumulating rollouts for batched PPO ({len(self.rollout_memory)}/{BATCH_SIZE})...")
            return

        logger.info("Executing Batched Trajectory PPO Update...")

        # We use a simple generalized advantage estimation-like (GAE) calculation over the batch
        states = torch.stack([r["state"] for r in self.rollout_memory])
        actions = torch.stack([r["action"] for r in self.rollout_memory])
        old_log_probs = torch.stack([r["log_prob"] for r in self.rollout_memory])
        old_values = torch.cat([r["value"] for r in self.rollout_memory])

        # Process rewards and advantages
        rewards = []
        for r in self.rollout_memory:
            rew = r.get("reward", 0.0)
            if torch.isnan(torch.tensor(rew)):
                rew = 0.0
            rewards.append(rew)

        rewards_tensor = torch.tensor(rewards, dtype=torch.float32)

        # Normalize rewards for stability
        if rewards_tensor.std() > 0:
            rewards_tensor = (rewards_tensor - rewards_tensor.mean()) / (rewards_tensor.std() + 1e-8)

        advantages = rewards_tensor - old_values.detach()
        if advantages.std() > 0:
            advantages = (advantages - advantages.mean()) / (advantages.std() + 1e-8)

        # Multi-epoch PPO update
        EPOCHS = 3
        total_actor_loss = 0.0
        total_critic_loss = 0.0

        for _ in range(EPOCHS):
            self.optimizer.zero_grad()

            action_means, new_values = self.policy_net(states)
            dist = torch.distributions.Normal(action_means, 0.1)
            new_log_probs = dist.log_prob(actions).sum(dim=-1) # Depending on action dim, sum over it

            ratio = torch.exp(new_log_probs - old_log_probs.detach())
            surr1 = ratio * advantages
            surr2 = torch.clamp(ratio, 1 - self.eps_clip, 1 + self.eps_clip) * advantages

            actor_loss = -torch.min(surr1, surr2).mean()
            critic_loss = nn.MSELoss()(new_values.squeeze(), rewards_tensor)

            loss = actor_loss + 0.5 * critic_loss
            loss.backward()

            torch.nn.utils.clip_grad_norm_(self.policy_net.parameters(), max_norm=1.0)
            self.optimizer.step()

            total_actor_loss += actor_loss.item()
            total_critic_loss += critic_loss.item()

        logger.info(f"PPO Batched Policy Updated. Avg Actor Loss: {total_actor_loss/EPOCHS:.4f}, Avg Critic Loss: {total_critic_loss/EPOCHS:.4f}")
        self.rollout_memory = []

